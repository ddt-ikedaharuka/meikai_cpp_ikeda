#include <iostream>

int main()
{
	/* int型の変数に50を代入 */
    int x = 50;

	/* int型の変数に100を代入 */
    int y = 100;

	/* コンソールに変数xの値を出力する */
    std::cout << "xの値は" << x << "です。";
	
	/* コンソールに変数yの値を出力する */
    std::cout << "yの値は" << y << "です。";
}

